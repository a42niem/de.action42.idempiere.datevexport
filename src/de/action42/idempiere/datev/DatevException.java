package de.action42.idempiere.datev;

public class DatevException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4522806234131495398L;

	public DatevException(String message, Throwable cause) {
		super(message, cause);
	}

	public DatevException(String message) {
		super(message);
	}

	public DatevException(Throwable cause) {
		super(cause);
	}

}
